metro = int(input('Digite aqui o valor (em metros) que deseja converter: '))
decimetro = metro * 10
centimetro = decimetro * 10
milimetro = centimetro * 10
decametro = metro / 10
hectometro = decametro / 10
kilometro = hectometro / 10
print('O valor em decímetros é de {}, em centimetros é de {} e em milimetros é de {}'.format(decimetro, centimetro, milimetro))
print('O valor em decâmetro é de {}, em hectômetro é de {} e en kilômetro é de {}'.format(decametro, hectometro, kilometro)) 
